// --- Ejemplo con let ---
let edad = 25; // Declara una variable llamada 'edad' con el valor 25.
console.log(edad); // Muestra: 25

edad = 26; // ¡El valor puede ser reasignado!
console.log(edad); // Muestra: 26

// --- Ejemplo con const ---
const nombre = "Ana"; // Declara una constante llamada 'nombre' con el valor "Ana".
console.log(nombre); // Muestra: "Ana"

// nombre = "Juan"; // Si intentas hacer esto, obtendrás un error.
